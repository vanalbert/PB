	<h1> As A Grain Of Mustard Seed </h1>
	<a title="As A Grain Of Mustard Seed" href="/as-a-grain-of-mustard-seed"><img height="235" width="300" class="inline-left" alt="As A Grain Of Mustard Seed" title="As A Grain Of Mustard Seed" src="http://local.prosperitybible.com/sites/all/themes/vanzen/images/accimg20.jpg"/></a>
	<p class="regular">Originally published as a series of articles, &ldquo;As A Grain of Mustard Seed&rdquo; is a story about FAITH and the financial struggles faced by a family and the role that FAITH played in overcoming those struggles.</p>
	<p class="regular">The title, &ldquo;As a Grain of Mustard Seed,&rdquo; is from a teaching by Jesus in the Gospel of Matthew.  In response to questions by His disciples as to why they could not heal a sick boy, Jesus responds, &ldquo;Because of your little faith . . . if you have FAITH the size of a mustard seed, you will say to this mountain, ‘Move from here to there,&lsquo; and it will move.  NOTHING will be impossible for you.&rdquo;</p>
	<p class="regular">And, there, is the Lesson of &ldquo;Mustard Seed&rdquo; in all of its simplicity: With Faith, anything is possible and nothing is impossible!</p>
	<p class="regular">&ldquo;As A Grain of Mustard Seed&rdquo; was Wattles&lsquo; final work.  He died before the final two chapters were published.</p>
	
	<a title="As A Grain Of Mustard Seed" href="/as-a-grain-of-mustard-seed"><img height="100" width="700" alt="As A Grain Of Mustard Seed" title="As A Grain Of Mustard Seed" src="http://local.prosperitybible.com/sites/all/themes/vanzen/images/divider.png"/></a>
	<p class="regular"><em>As a Grain of Mustard Seed </em>is a short story by Wallace D. Wattles that was originally published as a series of articles in monthly issues of a magazine.  The story details the hardship of a close family and the efforts undertaken to resolve a major financial obstacle and keep a family member from going to prison for alleged theft.  The story is about Faith, and while a fictional work by Mr. Wattles, it incorporates his teachings and principles into a case study of his prior works and how application of those teachings and principles &mdash; particularly, Faith &mdash; can solve any problem, no matter how impossible it may seem.</p>
	<p class="regular">The title, &ldquo;<em>As a Grain of Mustard Seed</em>,&rdquo; is derived from a teaching by Jesus Christ found in the Gospel of Matthew concerning a boy possessed by a demon.  The father brings the boy to Jesus, asking the Nazarene to cure his son and further advising Jesus that he had already brought his son to the Master&lsquo;s disciples, but they could not cure him.  Jesus instructs the man to bring Him his son, and upon doing so, Jesus cures the boy, driving the demon out.  <em>See</em> <strong>Matt.</strong> 17:14-18.</p>

	<p class="regular">Afterwards, Jesus&lsquo; disciples approach Him in private, asking &ldquo;Why could we not drive it [the demon] out?&rdquo;  Jesus responds to them &ldquo;<strong>Because of your little faith</strong>.  Amen, I say to you, if you have faith the size of a mustard seed, you will say to this mountain, ‘Move from here to there,&lsquo; and it will move.  Nothing will be impossible for you.&rdquo;  <strong>Matt.</strong> 17:19-21.</p>
	<p class="regular">And, there, is the lesson is all of its simplicity: With Faith, <em>anything</em> is possible; <em>nothing</em> is impossible.</p>
	<p class="regular">From all accounts, <em>As a Grain of Mustard Seed </em>was Mr. Wattles&lsquo; last work, as he met an untimely death at the young age of 51.  While not a significant literary achievement in the halls of fictional works, <em>As a Grain of Mustard Seed</em> is pure Wattles&lsquo; philosophy and a beautiful story from a relatively simple man about human struggle and the Faith necessary to endure and overcome.</p>



									<h2 class=vol-title">The Lost Sequel To ‘As A Grain Of Mustard Seed’ </h2>
									<img src="http://local.prosperitybible.com/sites/all/themes/vanzen/images/vol-5-5-img.jpg" class="inline-left" title="The Lost Sequel To ‘As A Grain Of Mustard Seed’ " alt="The Lost Sequel To ‘As A Grain Of Mustard Seed’ " height="235" width="300"/>
									<p class="regular">After Wattles’ untimely death at the age of 51, when the final two chapters of “As A Grain of Mustard Seed” were published in a magazine, the publisher announced a writing competition for a sequel.&nbsp; There was a winner to the publisher’s contest and “The Lost Sequel” is the winning story by Katherine Quinn – a conclusion to the story of Jason Packet, the only character in Wattles’ original story that failed to transform his wrong ways through the Power of FAITH.</span>	


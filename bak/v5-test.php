									<p class="regular"><em>As a Grain of Mustard Seed </em>by Wallace D. Wattles was originally published as a series of short stories in a magazine in 1910-1911.&nbsp; After Mr. Wattles died in 1911 at the age of 51, the final two chapters of the story were published, and in the monthly issue of the magazine in which those two chapters were published, the publisher announced a contest for a sequel to <em>As a Grain of Mustard Seed </em>b.&nbsp; Since Jason Packet was the only main character in original Wattles’ story who failed to transform his wrong ways and learn from the Power of Faith, the publisher was seeking a sequel wherein “Jason is saved.”</p>
									<p class="regular">There was a winner to the publisher’s contest and “The Lost Sequel” is the winning story by Katherine Quinn.&nbsp; A charming finish to a purely-Wattles tale.</p></div>								

									<h2 class=vol-title">As A Grain Of Mustard Seed </h2>
									<img src="http://local.prosperitybible.com/sites/all/themes/vanzen/images/vol-5-4-img.jpg" class="inline-left" title="As A Grain Of Mustard Seed " alt="As A Grain Of Mustard Seed " height="235" width="300"/>
									<p class="regular">Originally published as a series of articles, “As A Grain of Mustard Seed” is a story about FAITH and the financial struggles faced by a family and the role that FAITH played in overcoming those struggles.</p>
									<p class="regular">The title, “As a Grain of Mustard Seed,” is from a teaching by Jesus in the Gospel of Matthew.&nbsp; In response to questions by His disciples as to why they could not heal a sick boy, Jesus responds, “Because of your little faith . . . if you have FAITH the size of a mustard seed, you will say to this mountain, ‘Move from here to there,’ and it will move.&nbsp; NOTHING will be impossible for you.”</p>
									<p class="regular">And, there, is the Lesson of “Mustard Seed” in all of its simplicity: With Faith, anything is possible and nothing is impossible!</p>
									<p class="regular">“As A Grain of Mustard Seed” was Wattles’ final work.&nbsp; He died before the final two chapters were published.</p>								

									<div id="divider"> 
										<img src="http://local.prosperitybible.com/sites/all/themes/vanzen/images/divider.png" /> 
									</div><!-- #divider close -->	

									<p class="regular"><em>As a Grain of Mustard Seed </em>is a short story by Wallace D. Wattles that was originally published as a series of articles in monthly issues of a magazine.&nbsp; The story details the hardship of a close family and the efforts undertaken to resolve a major financial obstacle and keep a family member from going to prison for alleged theft.&nbsp; The story is about Faith, and while a fictional work by Mr. Wattles, it incorporates his teachings and principles into a case study of his prior works and how application of those teachings and principles – particularly, Faith – can solve any problem, no matter how impossible it may seem.</p>
									<p class="regular">The title, “<em>As a Grain of Mustard Seed</em>,” is derived from a teaching by Jesus Christ found in the Gospel of Matthew concerning a boy possessed by a demon.&nbsp; The father brings the boy to Jesus, asking the Nazarene to cure his son and further advising Jesus that he had already brought his son to the Master’s disciples, but they could not cure him.&nbsp; Jesus instructs the man to bring Him his son, and upon doing so, Jesus cures the boy, driving the demon out.&nbsp; <em>See</em> <strong>Matt.</strong> 17:14-18.</p>
									<p class="regular">Afterwards, Jesus’ disciples approach Him in private, asking “Why could we not drive it [the demon] out?”&nbsp; Jesus responds to them “<strong>Because of your little faith</strong>.&nbsp; Amen, I say to you, if you have faith the size of a mustard seed, you will say to this mountain, ‘Move from here to there,’ and it will move.&nbsp; Nothing will be impossible for you.”&nbsp; <strong>Matt.</strong> 17:19-21.</p>
									<p class="regular">And, there, is the lesson is all of its simplicity: With Faith, <em>anything</em> is possible; <em>nothing</em> is impossible.</p>
									<p class="regular">From all accounts, <em>As a Grain of Mustard Seed </em>was Mr. Wattles’ last work, as he met an untimely death at the young age of 51.&nbsp; While not a significant literary achievement in the halls of fictional works, <em>As a Grain of Mustard Seed</em> is pure Wattles’ philosophy and a beautiful story from a relatively simple man about human struggle and the Faith necessary to endure and overcome.</p>

									

<p class="regular">We learn from early childhood that exercise is important for our health and wellbeing, but why do so few realize that the same is true for our mind?  Why do only a few people achieve Greatness in their lives, while the majority of the masses live in mediocrity?  Why are even fewer individuals &ldquo;born to Lead,&rdquo; while even greater numbers of the masses resign to living a life of &ldquo;following?&rdquo; In The Science of Being Great, Later re-published as How to be a Genius, learn the very steps that lead toward Greatness and Power &mdash; and HOW you can implement and practice these Scientific Truths to achieve Greatness and Power &mdash; in YOUR life.</p>

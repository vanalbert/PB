<p class="regular">As with most things in life, it is preparation and planning that produces the outcomes that we admire &mdash; whether they be in relationships or other personal matters, corporate achievements or political success stories. The same is true of ourselves and by learning and APPLYING the Scientific Methods of Being Great , you can &mdash; and SHALL &mdash; achieve Extraordinary Greatness &mdash; in YOUR LIFE!</p>


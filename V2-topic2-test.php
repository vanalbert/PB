		<li>Constructive vs. Destructive Mental Attitudes &mdash; how to recognize the differences by paying attention to Your thought processes.</li>

		<li>The Power of Faith in attaining Success in any endeavor.</li>
		<li>&ldquo;The Matter of Attitude&rdquo; &mdash; Your own Mental Attitude towards Your own Abilities and why cultivating the proper Mental Attitude is critical to exploiting Your Abilities to their highest levels.</li>
		<li>Why &ldquo;Desire, in itself, has no power to bring you what you want,&rdquo; but how Desire may be utilized to fuel scientific methods for attaining that which is Desired.</li>
		<li>How to &ldquo;mentally unite&rdquo; Yourself to that which you Desire in order to manifest the Desired subject in the physical world.</li>
		<li>Desire is the Mental Force that connects You to and moves you towards the object of Desire; Expectation is the complimentary Mental Force that moves the object of Desire towards You.</li>
		<li>The Power of Visualization (Image Making) and why You should NEVER Visualize that which you do not Desire.</li>

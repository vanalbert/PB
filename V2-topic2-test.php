		<li>&ldquo;Conspicuous Waste&rdquo; &mdash; what it is and why it is to be avoided in order to attract Prosperity, Abundance and Wealth.</li>
		<li>How to dismiss the Thought Habit that You &ldquo;cannot afford&rdquo; what you want in Life and that by &ldquo;learning to talk wealth, health and progress, you can have the best of everything.&rdquo;</li>
		<li>Why being &ldquo;economic&rdquo; and &ldquo;thrifty&rdquo; and &ldquo;living cheaply&rdquo; actually impedes the flow of Wealth, Abundance and Prosperity.</li>

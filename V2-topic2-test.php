		<li>The &ldquo;sympathy habit&rdquo; &mdash; what it is, why it is so destructive, and why it is hard to break.</li>
		<li>How to &ldquo;sympathize health&rdquo; when afflicted with poor health, so as to gain good Health.</li>
		<li>The effect of &ldquo;stressful&rdquo; or agitating work on Your Health and how to adopt habits and methods to override such destructive effects on Body, Mind and Spirit.</li>
		<li>&ldquo;Hate Thought&rdquo; &mdash; what is it and why You must rid it completely from your Conscious and Subconscious Mind.</li>


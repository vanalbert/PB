	<h1> Lessons In Constructive Science </h1>
	<a title="Lessons In Constructive Science" href="/lessons-in-constructive-science"><img height="235" width="300" class="inline-left" alt="Lessons In Constructive Science" title="Lessons In Constructive Science" src="http://local.prosperitybible.com/sites/all/themes/vanzen/images/accimg5.jpg"/></a>
	<p class="regular">God wants us all to Live an Abundant, Prosperous and Wealthy life - whether financial or otherwise. It is Nature&lsquo;s intent that ALL things &mdash; and most particularly, human life &mdash; GROW.  The purpose of Nature is the advancement and unfoldment of life and Desire is possibility seeking expression, or function seeking performance.</p>
	<p class="regular">In this series of fifteen Lessons, you will learn to distinguish between merely wanting (wishing) and Willing (Desiring) in order to focus your mental, emotional and spiritual being to be receptive to only that which you truly Desire through use and application of proven Constructive Science methods.</p>
	<p class="regular">&ldquo;As a man thinketh in his heart, so shall he be.&rdquo;  Under the Laws of the Universe, we inherently attract what we think. When we think negatively, we focus upon that which we don&lsquo;t want, thereby attracting the very thing that we FEAR and NOT what we want; likewise, when we think positively, and focus upon our true Desires, we thereby attract that which we seek.  In Lessons in Constructive Science, you will learn HOW maximize your positive Mental Attitude to cultivate and exploit YOUR God-given abilities and transform YOUR thought processes into real, tangible RESULTS.</p>
	<p class="regular">Learning to distinguish between Constructive and Destructive Mental Attitudes will change the way YOU think.  Changing the WAY YOU THINK will change the RESULTS in YOUR Life..</p>

